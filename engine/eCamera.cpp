#include "eCamera.h"

// ------------------------------------
Camera::Camera() :
// ------------------------------------
	m_x(0),
	m_y(0),
	m_zoom(0)
{

}

// ------------------------------------
Camera::Camera(float* x, float* y, float* zoom) :
// ------------------------------------
	m_x(x),
	m_y(y),
	m_zoom(zoom)
{

}

// ------------------------------------
float Camera::GetX()
// ------------------------------------
{
	if (m_x)
		return *m_x;
	return 0;
}

// ------------------------------------
float Camera::GetY()
// ------------------------------------
{
	if (m_y)
		return *m_y;
	return 0;
}

// ------------------------------------
float Camera::GetZoom()
// ------------------------------------
{
	if (m_zoom)
		return *m_zoom;
	return 1;
}
